package lesson22.shortexamples;


//Data class
//Value object
//DTO - data transfer object
//Java Bean
public class IsIsGet {
	
	private int rost;
	private double ves;
	private boolean horosho;
	
	public int getRost() {
		return rost;
	}
	public void setRost(int rost) {
		this.rost = rost;
	}
	public double getVes() {
		return ves;
	}
	public void setVes(double ves) {
		this.ves = ves;
	}
	public boolean isHorosho() {
		return horosho;
	}
	public void setHorosho(boolean horosho) {
		this.horosho = horosho;
	}
	
	
}
